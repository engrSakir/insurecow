     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $inputs=[
            'name'=>'required',
            'email'=>'required',
            'phone'=>'required',
            'company'=>'required',
        ];
        $this->validate($request,$inputs);
        $send=New Send();
        $send->name=$request->name;
        $send->email=$request->email;
        $send->phone=$request->phone;
        $send->company=$request->company;
        $send->save();
        return redirect()->back()->with('send','Thank you for your interest.Our Team will get in touch with you shortly.');


    }

